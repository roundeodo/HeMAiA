# Dual-VersaCore MoE datagen
#
# 这个脚本负责生成两类东西：
#   1. workload 运行时要喂给硬件的静态输入/权重数据
#   2. 调试时能对照的 golden 结果
#
# 它不参与 Phase 4 动态调度；它只负责把“数据长什么样”准备好。
#
# Hardware: snax_dual_versacore_int16x4_rebalanced_cluster
#   A: INT16, B: INT4 nibble-packed, D: INT32
#
# B tile layout (K-continuous per N-unit, K-major within tile):
#   B[expert, N2, K1, N1, tileSize, meshCol]
#   每个 byte 打包 2 个 INT4：低 4bit 是前一个， 高 4bit 是后一个。
#
# down_B layout: [expert, N2_down, K1_down, N1_down, tileSize, meshCol] (INT4 packed)
# A tile layout: [M2, M1, K1, meshRow, tileSize] (INT16)
# D tile layout: [M1, N1_total, meshRow, meshCol] (INT32)

import numpy as np
import argparse
import pathlib
import hjson
import sys
import os
import re

CURRENT_DIR = os.path.dirname(os.path.abspath(__file__))

sys.path.append(
    os.path.join(os.path.dirname(__file__), "../../../../../../../../util/sim/")
)
from data_utils import (
    format_scalar_definition,
    format_vector_definition,
)

np.random.seed(320)


def log(msg):
    print(f"[datagen] {msg}", file=sys.stderr, flush=True)


def parse_header_config(head_path):
    cfg = {}
    pattern = re.compile(r"#define\s+(\w+)\s+([-+]?[\d\.]+)")
    with open(head_path) as f:
        for line in f:
            m = pattern.search(line)
            if m:
                try:
                    cfg[m.group(1)] = eval(m.group(2).strip())
                except Exception:
                    pass
    return cfg


def find_top_k(scores_2D, k):
    return np.argsort(scores_2D, axis=1)[:, ::-1][:, :k].astype(np.uint16)


def pack_int4(values_flat):
    """Pack INT4 values: each pair (lo=values[2i], hi=values[2i+1]) into one byte."""
    arr = np.array(values_flat, dtype=np.int8).flatten()
    if len(arr) % 2 != 0:
        arr = np.append(arr, 0)
    packed = np.zeros(len(arr) // 2, dtype=np.uint8)
    lo = arr[0::2].astype(np.uint8) & 0x0F
    hi = arr[1::2].astype(np.uint8) & 0x0F
    packed = lo | (hi << 4)
    return packed.astype(np.uint8)


def emit_header_file(**kw):
    lines = [
        "// Auto-generated by multi_cluster_MoE_datagen.py",
        "// This header contains test input / weight blobs and some golden reference arrays.",
        "// If the numbers look odd, read multi_cluster_MoE_datagen.py instead of editing this file by hand.",
        "#include <stdint.h>",
        "#include <stddef.h>",
    ]
    lines += emit_moe_data(**kw)
    return "\n\n".join(lines)


def emit_moe_data(**kw):
    head = os.path.join(CURRENT_DIR, "MoE_common_variable.h")
    cfg = parse_header_config(head)
    data_str = []

    # Support both old and new template key
    core_tmpl = kw.get("snax_versacore_core_template") or kw.get(
        "snax_dual_versacore_int16x4_core_template"
    )
    if core_tmpl is None:
        raise KeyError("No versacore core template found in hwcfg")

    data_type = 0
    acc = core_tmpl["snax_acc_cfg"][0]
    ashape = kw["array_shape"]
    meshRow = acc["snax_versacore_spatial_unrolling"][data_type][ashape][0]
    tileSize = acc["snax_versacore_spatial_unrolling"][data_type][ashape][1]
    meshCol = acc["snax_versacore_spatial_unrolling"][data_type][ashape][2]

    # Per-matrix shape overrides
    # input_A / static tiled A: can use different (meshRow, tileSize) than hwcfg default
    meshRow_A = int(kw.get("A_meshRow", meshRow))
    tileSize_A = int(kw.get("A_tileSize", tileSize))
    # down projection B: may use wider meshCol (streamer configured separately)
    meshCol_down = int(kw.get("down_meshCol", meshCol))
    down_vc_meshCol = meshCol
    if meshCol_down != 2 * down_vc_meshCol:
        raise ValueError("dual-VC down projection expects down_meshCol == 2 * meshCol")

    rM1, rN1, rK1 = kw["router_M1"], kw["router_N1"], kw["router_K1"]
    rM2, rN2, rK2 = kw["router_M2"], kw["router_N2"], kw["router_K2"]
    iM1, iN1, iK1 = kw["indiv_M1"], kw["indiv_N1"], kw["indiv_K1"]
    iM2, iN2, iK2 = kw["indiv_M2"], kw["indiv_N2"], kw["indiv_K2"]
    idM1, idN1, idK1 = (
        kw.get("indiv_down_M1", iM1),
        kw.get("indiv_down_N1", iN1),
        kw.get("indiv_down_K1", 4),
    )
    idM2, idN2, idK2 = (
        kw.get("indiv_down_M2", iM2),
        kw.get("indiv_down_N2", iN2),
        kw.get("indiv_down_K2", 1),
    )
    sM1, sN1, sK1 = (
        kw.get("shared_M1", iM1),
        kw.get("shared_N1", iN1),
        kw.get("shared_K1", iK1),
    )
    sM2, sN2, sK2 = (
        kw.get("shared_M2", iM2),
        kw.get("shared_N2", iN2),
        kw.get("shared_K2", iK2),
    )
    sdM1, sdN1, sdK1 = (
        kw.get("shared_down_M1", sM1),
        kw.get("shared_down_N1", sN1),
        kw.get("shared_down_K1", idK1),
    )
    sdM2, sdN2, sdK2 = (
        kw.get("shared_down_M2", sM2),
        kw.get("shared_down_N2", sN2),
        kw.get("shared_down_K2", idK2),
    )
    num_shared = int(cfg.get("shared_expert_number_k", 0))

    num_indiv_experts = int(cfg["expert_number_each_layer"])
    top_k = int(cfg["individual_expert_number_k"])
    M_total = rM2 * rM1 * meshRow_A  # uses A_meshRow (may differ from hwcfg meshRow)
    K_total = rK2 * rK1 * tileSize  # K total (B's tileSize, = K_logical = 64)
    assert K_total % tileSize_A == 0, "input K_total must be divisible by A_tileSize"
    K1_A = K_total // tileSize_A  # K1 for A storage: K_total / A_tileSize
    N_router = rN2 * rN1 * meshCol
    N_indiv = iN2 * iN1 * meshCol
    N_shared = sN2 * sN1 * meshCol
    N_indiv_down = idN2 * idN1 * meshCol_down  # uses down meshCol override
    K_indiv_down = idK2 * idK1 * tileSize
    K_shared_down = sdK2 * sdK1 * tileSize

    assert (
        N_router == num_indiv_experts
    ), f"router N={N_router} != num_indiv_experts={num_indiv_experts}"
    assert (
        K_indiv_down == N_indiv
    ), f"individual down K={K_indiv_down} != gate/up hidden N={N_indiv}"
    assert (
        K_shared_down == N_shared
    ), f"shared down K={K_shared_down} != gate/up hidden N={N_shared}"

    log(f"S{ashape}: meshRow={meshRow} tileSize={tileSize} meshCol={meshCol}")
    log(
        f"A_meshRow={meshRow_A} A_tileSize={tileSize_A} K1_A={K1_A} "
        f"down_meshCol={meshCol_down} down_vc_meshCol={down_vc_meshCol}"
    )
    log(f"M_total={M_total} K_total={K_total} N_router={N_router} N_indiv={N_indiv}")
    log(f"N_indiv_down={N_indiv_down} K_indiv_down={K_indiv_down}")

    # Scalars
    for name, val in [
        ("array_shape", ashape),
        ("meshRow", meshRow),
        ("tileSize", tileSize),
        ("meshCol", meshCol),
        ("meshRow_A", meshRow_A),
        ("tileSize_A", tileSize_A),
        ("K1_A", K1_A),
        ("meshCol_down", meshCol_down),
        ("down_vc_meshCol", down_vc_meshCol),
        ("router_M1", rM1),
        ("router_N1", rN1),
        ("router_K1", rK1),
        ("router_M2", rM2),
        ("router_N2", rN2),
        ("router_K2", rK2),
        ("indiv_M1", iM1),
        ("indiv_N1", iN1),
        ("indiv_K1", iK1),
        ("indiv_M2", iM2),
        ("indiv_N2", iN2),
        ("indiv_K2", iK2),
        ("indiv_down_M1", idM1),
        ("indiv_down_N1", idN1),
        ("indiv_down_K1", idK1),
        ("indiv_down_M2", idM2),
        ("indiv_down_N2", idN2),
        ("indiv_down_K2", idK2),
        ("shared_M1", sM1),
        ("shared_N1", sN1),
        ("shared_K1", sK1),
        ("shared_M2", sM2),
        ("shared_N2", sN2),
        ("shared_K2", sK2),
        ("shared_down_M1", sdM1),
        ("shared_down_N1", sdN1),
        ("shared_down_K1", sdK1),
        ("shared_down_M2", sdM2),
        ("shared_down_N2", sdN2),
        ("shared_down_K2", sdK2),
        ("num_shared_experts", num_shared),
        ("M_total", M_total),
        ("K_total_input", K_total),
        ("N_router", N_router),
        ("N_indiv", N_indiv),
        ("N_indiv_down", N_indiv_down),
        ("K_indiv_down", K_indiv_down),
        ("num_indiv_experts", num_indiv_experts),
        ("top_k", top_k),
        ("addNonZeroC", kw["addNonZeroC"]),
        ("addZeroC", kw["addZeroC"]),
        ("accumPrevC", kw["accumPrevC"]),
    ]:
        data_str += [format_scalar_definition("uint32_t", name, val)]

    # -------- input_A (INT16, 2 bytes/element) --------
    # input_A is token-contiguous: (M2, M1, meshRow_A, K1_A, tileSize_A).
    # input_A_tiled is the same logical tensor in streamer layout for static GEMMs:
    # (M2, M1, K1_A, meshRow_A, tileSize_A).
    log("generating input_A (INT16)")
    A_phys = np.random.randint(
        -256, 255, size=(rM2, rM1, meshRow_A, K1_A, tileSize_A), dtype=np.int16
    )
    A_flat = A_phys.view(np.uint8).reshape(-1)
    pad = (-len(A_flat)) % 64
    if pad:
        A_flat = np.pad(A_flat, (0, pad), constant_values=0)
    data_str += [format_vector_definition("uint8_t", "input_A", A_flat)]

    A_tiled_phys = np.ascontiguousarray(A_phys.transpose(0, 1, 3, 2, 4))
    A_tiled_flat = A_tiled_phys.view(np.uint8).reshape(-1)
    pad = (-len(A_tiled_flat)) % 64
    if pad:
        A_tiled_flat = np.pad(A_tiled_flat, (0, pad), constant_values=0)
    data_str += [format_vector_definition("uint8_t", "input_A_tiled", A_tiled_flat)]

    # -------- router_B (INT4 packed, K-continuous per N-unit) --------
    # Shape before packing: (rN2, rK2*rK1, rN1, tileSize, meshCol)
    log("generating router_B (INT4 packed)")
    rB_values = np.random.randint(
        -7, 7, size=(rN2, rK2 * rK1, rN1, tileSize, meshCol), dtype=np.int8
    )
    data_str += [format_vector_definition("uint8_t", "router_B", pack_int4(rB_values))]

    # -------- TopK golden --------
    log("computing TopK")
    A_mat = A_phys.reshape(M_total, K_total).astype(np.int32)
    # Dequantize INT4 values for golden computation (scale by 1 for simplicity)
    rB_mat = (
        rB_values.transpose(0, 2, 4, 1, 3).reshape(N_router, K_total).T.astype(np.int32)
    )
    scores = A_mat @ rB_mat
    topk_idx = find_top_k(scores, top_k)
    exp_counts = np.zeros(num_indiv_experts, dtype=np.uint32)
    for t in range(M_total):
        for s in range(top_k):
            exp_counts[int(topk_idx[t, s])] += 1
    log(f"expert token counts: {exp_counts.tolist()}")
    data_str += [
        format_vector_definition(
            "uint16_t", "golden_topk_indices", topk_idx.reshape(-1)
        )
    ]
    data_str += [
        format_vector_definition("uint32_t", "golden_expert_token_counts", exp_counts)
    ]

    # -------- indiv_gate_B (INT4 packed, 128B aligned for anti-phase SPM bank access) --------
    # Shape before packing: (num_indiv_experts, N2, K1, N1, tileSize, meshCol)
    log("generating indiv_gate_B (INT4 packed, 128B aligned)")
    gB_phys = np.random.randint(
        -7,
        7,
        size=(num_indiv_experts, iN2, iK2 * iK1, iN1, tileSize, meshCol),
        dtype=np.int8,
    )
    gB_packed = pack_int4(gB_phys)
    data_str += [
        format_vector_definition("uint8_t", "indiv_gate_B", gB_packed, alignment=128)
    ]

    # 64-byte SPM superbank alignment pad (anti-phase for up_B)
    data_str += [
        format_vector_definition(
            "uint8_t",
            "_spm_bank_align_pad_indiv",
            np.zeros(64, dtype=np.uint8),
            alignment=64,
        )
    ]

    # -------- indiv_up_B (INT4 packed, 64B aligned, anti-phase to gate_B) --------
    log("generating indiv_up_B (INT4 packed, anti-phase)")
    uB_phys = np.random.randint(
        -7,
        7,
        size=(num_indiv_experts, iN2, iK2 * iK1, iN1, tileSize, meshCol),
        dtype=np.int8,
    )
    uB_packed = pack_int4(uB_phys)
    data_str += [
        format_vector_definition("uint8_t", "indiv_up_B", uB_packed, alignment=64)
    ]

    # -------- indiv_down_B (INT4 packed, for down projection) --------
    # Shape: (num_indiv_experts, vc_half, N2_down, K1_down, N1_down, tileSize, meshCol)
    # The two vc_half blocks are contiguous so dual_vc_gemm_full can use B0/B1 bases.
    log("generating indiv_down_B (INT4 packed)")
    dB_phys = np.random.randint(
        -7,
        7,
        size=(num_indiv_experts, 2, idN2, idK2 * idK1, idN1, tileSize, down_vc_meshCol),
        dtype=np.int8,
    )
    dB_packed = pack_int4(dB_phys)
    data_str += [
        format_vector_definition("uint8_t", "indiv_down_B", dB_packed, alignment=64)
    ]

    # -------- shared_gate_B / shared_up_B / shared_down_B --------
    if num_shared > 0:
        log(
            f"generating shared_gate_B ({num_shared} shared experts, INT4, 128B aligned)"
        )
        sgB_phys = np.random.randint(
            -7,
            7,
            size=(num_shared, sN2, sK2 * sK1, sN1, tileSize, meshCol),
            dtype=np.int8,
        )
        data_str += [
            format_vector_definition(
                "uint8_t", "shared_gate_B", pack_int4(sgB_phys), alignment=128
            )
        ]
        data_str += [
            format_vector_definition(
                "uint8_t",
                "_spm_bank_align_pad_shared",
                np.zeros(64, dtype=np.uint8),
                alignment=64,
            )
        ]
        log(f"generating shared_up_B ({num_shared} shared experts, INT4, anti-phase)")
        suB_phys = np.random.randint(
            -7,
            7,
            size=(num_shared, sN2, sK2 * sK1, sN1, tileSize, meshCol),
            dtype=np.int8,
        )
        data_str += [
            format_vector_definition(
                "uint8_t", "shared_up_B", pack_int4(suB_phys), alignment=64
            )
        ]
        log(f"generating shared_down_B ({num_shared} shared experts, INT4)")
        sdB_phys = np.random.randint(
            -7,
            7,
            size=(num_shared, 2, sdN2, sdK2 * sdK1, sdN1, tileSize, down_vc_meshCol),
            dtype=np.int8,
        )
        data_str += [
            format_vector_definition(
                "uint8_t", "shared_down_B", pack_int4(sdB_phys), alignment=64
            )
        ]

    # -------- Stride / tile-size constants (bytes) --------
    # A_token_bytes = one logical token vector = K_total INT16 elements.
    # input_A is token-contiguous: [M2, M1, meshRow_A, K1_A, tileSize_A].
    A_token_bytes = int(K1_A * tileSize_A * 2)
    A_total_bytes = int(M_total * A_token_bytes)
    # A tile (M1 tokens, used only when M1>1 / shared path): INT16 → 2 bytes/element
    rA_tile = int(rM1 * K1_A * meshRow_A * tileSize_A * 2)
    iA_tile = int(iM1 * K1_A * meshRow_A * tileSize_A * 2)
    sA_tile = int(sM1 * K1_A * meshRow_A * tileSize_A * 2)
    # B tile: INT4 packed → meshCol*tileSize/2 bytes; gate/up use standard meshCol=4
    rB_tile = int(rK1 * rN1 * tileSize * meshCol // 2)
    iB_tile = int(iK1 * iN1 * tileSize * meshCol // 2)
    idB_tile = int(idK1 * idN1 * tileSize * down_vc_meshCol // 2)
    sB_tile = int(sK1 * sN1 * tileSize * meshCol // 2)
    sdB_tile = int(
        sdK1 * sdN1 * tileSize * down_vc_meshCol // 2
    )
    # D tile: INT32 → 4 bytes/element; M dim uses meshRow_A, N dim uses per-proj meshCol
    rD_tile = int(rM1 * rN1 * meshRow_A * meshCol * 4)
    iD_tile = int(iM1 * iN1 * meshRow_A * meshCol * 4)
    idD_tile = int(idM1 * idN1 * meshRow_A * down_vc_meshCol * 4)
    sD_tile = int(sM1 * sN1 * meshRow_A * meshCol * 4)
    sdD_tile = int(sdM1 * sdN1 * meshRow_A * down_vc_meshCol * 4)
    # Expert strides for INT4 packed B
    iB_expert_stride = int(iN2 * iK2 * iK1 * iN1 * tileSize * meshCol // 2)
    iB_n2_stride = int(iK2 * iK1 * iN1 * tileSize * meshCol // 2)
    idB_expert_stride = int(2 * idN2 * idK2 * idK1 * idN1 * tileSize * down_vc_meshCol // 2)
    idB_n2_stride = int(idK2 * idK1 * idN1 * tileSize * down_vc_meshCol // 2)
    sB_expert_stride = int(sN2 * sK2 * sK1 * sN1 * tileSize * meshCol // 2)
    sB_n2_stride = int(sK2 * sK1 * sN1 * tileSize * meshCol // 2)
    sdB_expert_stride = int(2 * sdN2 * sdK2 * sdK1 * sdN1 * tileSize * down_vc_meshCol // 2)
    sdB_n2_stride = int(sdK2 * sdK1 * sdN1 * tileSize * down_vc_meshCol // 2)

    for name, val in [
        # A single-token DMA granularity
        ("A_token_bytes", A_token_bytes),
        ("A_total_bytes", A_total_bytes),
        ("indiv_B_expert_stride", iB_expert_stride),
        ("indiv_B_n2_stride", iB_n2_stride),
        ("indiv_down_B_expert_stride", idB_expert_stride),
        ("indiv_down_B_n2_stride", idB_n2_stride),
        ("router_D_tilesize", rD_tile),
        ("indiv_D_tilesize", iD_tile),
        ("indiv_down_D_tilesize", idD_tile),
        ("router_A_tilesize", rA_tile),
        ("router_B_tilesize", rB_tile),
        ("indiv_B_tilesize", iB_tile),
        ("indiv_down_B_tilesize", idB_tile),
        ("shared_A_tilesize", sA_tile),
        ("shared_B_tilesize", sB_tile),
        ("shared_D_tilesize", sD_tile),
        ("shared_down_D_tilesize", sdD_tile),
        ("shared_down_B_tilesize", sdB_tile),
        ("shared_B_expert_stride", sB_expert_stride),
        ("shared_B_n2_stride", sB_n2_stride),
        ("shared_down_B_expert_stride", sdB_expert_stride),
        ("shared_down_B_n2_stride", sdB_n2_stride),
    ]:
        data_str += [format_scalar_definition("uint32_t", name, val)]

    log(
        f"Done. M={M_total} K={K_total} N_router={N_router} N_indiv={N_indiv} "
        f"N_indiv_down={N_indiv_down} E={num_indiv_experts} top_k={top_k} "
        f"iB_stride={iB_expert_stride}B"
    )
    return data_str


def get_args():
    parser = argparse.ArgumentParser(
        description="Generate static data and golden tensors for multi_cluster_MoE"
    )
    parser.add_argument(
        "-c",
        "--cfg",
        type=pathlib.Path,
        default=pathlib.Path(CURRENT_DIR) / "params.hjson",
    )
    parser.add_argument(
        "--hwcfg",
        type=pathlib.Path,
        default=pathlib.Path(CURRENT_DIR)
        / "../../../../../../../../deps/snitch_cluster/target/"
        "snitch_cluster/cfg/snax_dual_versacore_int16x4_rebalanced_cluster.hjson",
    )
    parser.add_argument("--emit_mini_golden", action="store_true")
    return parser.parse_args()


def main():
    args = get_args()
    with args.cfg.open() as f:
        pcfg = hjson.loads(f.read())
    with args.hwcfg.open() as f:
        hcfg = hjson.loads(f.read())
    merged = {**pcfg, **hcfg, "emit_mini_golden": args.emit_mini_golden}
    print(emit_header_file(**merged))


if __name__ == "__main__":
    main()
