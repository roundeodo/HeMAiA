def emit_header_file(**kwargs):
    emit_str = [
        "// Auto-generated by single_cluster_MoE_datagen.py (INT16×INT4 dual-VC)",
        "#include <stdint.h>",
        "#include <stddef.h>",
    ]
    emit_str += emit_MoE_data(**kwargs)
    return "\n\n".join(emit_str)


def parse_header_config(head_path):
    MoE_config = {}
    pattern = re.compile(r"#define\s+(\w+)\s+([-+]?[\d\.]+)")
    with open(head_path, "r") as f:
        for line in f:
            match = pattern.search(line)
            if match:
                key = match.group(1)
                value_str = match.group(2).strip()
                MoE_config[key] = eval(value_str)
    return MoE_config


def pack_int4(values_flat):
    """Pack INT4 values: pair (lo=values[2i], hi=values[2i+1]) into one byte."""
    arr = np.array(values_flat, dtype=np.int8).flatten()
    if len(arr) % 2 != 0:
        arr = np.append(arr, 0)
    lo = arr[0::2].astype(np.uint8) & 0x0F
    hi = arr[1::2].astype(np.uint8) & 0x0F
    return (lo | (hi << 4)).astype(np.uint8)


def find_top_k_golden_model(scores_2D, expert_number_k):
    top_k_indices = np.argsort(scores_2D, axis=1)[:, ::-1][:, :expert_number_k]
    return top_k_indices.astype(np.uint16)


def softmax_golden_model(raw_scores_2D, expert_number_k, softmax_scale):
    M_total = raw_scores_2D.shape[0]
    top_k_indices = find_top_k_golden_model(raw_scores_2D, expert_number_k)
    top_k_probability = np.zeros((M_total, expert_number_k), dtype=np.uint32)
    for m in range(M_total):
        selected_scores = raw_scores_2D[m, top_k_indices[m]]
        max_score = np.max(selected_scores)
        exp_values = np.exp(selected_scores - max_score).astype(np.float32)
        exp_sum = np.sum(exp_values)
        probability = ((exp_values / exp_sum) * softmax_scale).astype(np.uint32)
        top_k_probability[m] = probability
    return top_k_indices, top_k_probability


def swish_glu_golden_model(gate_data, up_data, scale_in, scale_out):
    gate_f = gate_data.astype(np.float32) * scale_in
    up_f = up_data.astype(np.float32) * scale_in
    swish_gate = (1.0 / (1.0 + np.exp(-gate_f))) * gate_f
    result_f = swish_gate * up_f
    result_i32 = (result_f * scale_out).astype(np.int32)
    return np.clip(result_i32, -128, 127).astype(np.int8)


def emit_MoE_data(**kwargs):
    MoE_config = parse_header_config(os.path.join(CURRENT_DIR, "MoE_common_variable.h"))
    data_str = []
    full_golden_data = kwargs.get("full_golden_data", False)

    # ---- Resolve hardware template (single-VC or dual-VC) ----
    core_tmpl = kwargs.get("snax_versacore_core_template") or kwargs.get(
        "snax_dual_versacore_int16x4_core_template"
    )
    if core_tmpl is None:
        raise KeyError(
            "No versacore core template found in hw config. "
            "Expected 'snax_versacore_core_template' or "
            "'snax_dual_versacore_int16x4_core_template'."
        )

    data_type = 0
    array_shape = kwargs["array_shape"]
    snax_acc_cfg = core_tmpl["snax_acc_cfg"][0]
    meshRow = snax_acc_cfg["snax_versacore_spatial_unrolling"][data_type][array_shape][
        0
    ]
    tileSize = snax_acc_cfg["snax_versacore_spatial_unrolling"][data_type][array_shape][
        1
    ]
    meshCol = snax_acc_cfg["snax_versacore_spatial_unrolling"][data_type][array_shape][
        2
    ]

    # Per-matrix hardware overrides (same convention as multi_cluster_MoE)
    meshRow_A = int(kwargs.get("A_meshRow", meshRow))
    tileSize_A = int(kwargs.get("A_tileSize", tileSize))
    meshCol_down = int(kwargs.get("down_meshCol", meshCol))
    vc_meshCol_down = meshCol  # single-VC portion of the down meshCol

    if meshCol_down != 2 * vc_meshCol_down:
        raise ValueError(
            f"down_meshCol={meshCol_down} must equal 2 × meshCol={vc_meshCol_down} "
            "(two VCs concatenated for down projection)."
        )

    # ---- Unpack loop-count parameters ----
    rM1 = kwargs["router_M1"]
    rK1 = kwargs["router_K1"]
    rN1 = kwargs["router_N1"]
    rM2 = kwargs["router_M2"]
    rK2 = kwargs["router_K2"]
    rN2 = kwargs["router_N2"]
    sM1 = kwargs["shared_swish_glu_M1"]
    sK1 = kwargs["shared_swish_glu_K1"]
    sN1 = kwargs["shared_swish_glu_N1"]
    sM2 = kwargs["shared_swish_glu_M2"]
    sK2 = kwargs["shared_swish_glu_K2"]
    sN2 = kwargs["shared_swish_glu_N2"]
    sdM1 = kwargs["shared_down_projection_M1"]
    sdK1 = kwargs["shared_down_projection_K1"]
    sdN1 = kwargs["shared_down_projection_N1"]
    sdM2 = kwargs["shared_down_projection_M2"]
    sdK2 = kwargs["shared_down_projection_K2"]
    sdN2 = kwargs["shared_down_projection_N2"]
    iM1 = kwargs["individual_swish_glu_M1"]
    iK1 = kwargs["individual_swish_glu_K1"]
    iN1 = kwargs["individual_swish_glu_N1"]
    iM2 = kwargs["individual_swish_glu_M2"]
    iK2 = kwargs["individual_swish_glu_K2"]
    iN2 = kwargs["individual_swish_glu_N2"]
    idM1 = kwargs["individual_down_projection_M1"]
    idK1 = kwargs["individual_down_projection_K1"]
    idN1 = kwargs["individual_down_projection_N1"]
    idM2 = kwargs["individual_down_projection_M2"]
    idK2 = kwargs["individual_down_projection_K2"]
    idN2 = kwargs["individual_down_projection_N2"]

    num_shared_experts = int(MoE_config["shared_expert_number_k"])
    num_indiv_experts = int(MoE_config["expert_number_each_layer"])
    top_k = int(MoE_config["individual_expert_number_k"])

    # ---- Derived logical dimensions ----
    K_total = rK2 * rK1 * tileSize  # total input K (uses B's tileSize)
    K1_A = K_total // tileSize_A  # K-tiles for A storage
    M_total = rM2 * rM1 * meshRow_A  # total tokens (uses A_meshRow)
    N_router = rN2 * rN1 * meshCol  # router output size
    N_hidden = sN2 * sN1 * meshCol  # gate/up hidden dim
    N_out = idN2 * idN1 * meshCol_down  # down proj output dim

    log_progress(
        f"array_shape={array_shape} meshRow={meshRow} tileSize={tileSize} meshCol={meshCol} | "
        f"meshRow_A={meshRow_A} tileSize_A={tileSize_A} K1_A={K1_A} "
        f"meshCol_down={meshCol_down} vc_meshCol_down={vc_meshCol_down}"
    )
    log_progress(
        f"M_total={M_total} K_total={K_total} N_router={N_router} "
        f"N_hidden={N_hidden} N_out={N_out}"
    )

    # ---- Emit scalar params ----
    for name, val in [
        ("array_shape", array_shape),
        ("meshRow", meshRow),
        ("tileSize", tileSize),
        ("meshCol", meshCol),
        ("meshCol_down", meshCol_down),
        ("meshRow_A", meshRow_A),
        ("tileSize_A", tileSize_A),
        ("K1_A", K1_A),
        ("router_M1", rM1),
        ("router_N1", rN1),
        ("router_K1", rK1),
        ("router_M2", rM2),
        ("router_N2", rN2),
        ("router_K2", rK2),
        ("shared_swish_glu_M1", sM1),
        ("shared_swish_glu_N1", sN1),
        ("shared_swish_glu_K1", sK1),
        ("shared_swish_glu_M2", sM2),
        ("shared_swish_glu_N2", sN2),
        ("shared_swish_glu_K2", sK2),
        ("shared_down_projection_M1", sdM1),
        ("shared_down_projection_N1", sdN1),
        ("shared_down_projection_K1", sdK1),
        ("shared_down_projection_M2", sdM2),
        ("shared_down_projection_N2", sdN2),
        ("shared_down_projection_K2", sdK2),
        ("individual_swish_glu_M1", iM1),
        ("individual_swish_glu_N1", iN1),
        ("individual_swish_glu_K1", iK1),
        ("individual_swish_glu_M2", iM2),
        ("individual_swish_glu_N2", iN2),
        ("individual_swish_glu_K2", iK2),
        ("individual_down_projection_M1", idM1),
        ("individual_down_projection_N1", idN1),
        ("individual_down_projection_K1", idK1),
        ("individual_down_projection_M2", idM2),
        ("individual_down_projection_N2", idN2),
        ("individual_down_projection_K2", idK2),
        ("addNonZeroC", kwargs["addNonZeroC"]),
        ("addZeroC", kwargs["addZeroC"]),
        ("accumPrevC", kwargs["accumPrevC"]),
    ]:
        data_str += [format_scalar_definition("uint32_t", name, val)]

    assert (
        sum([kwargs["addNonZeroC"], kwargs["addZeroC"], kwargs["accumPrevC"]]) == 1
    ), "Exactly one of addNonZeroC / addZeroC / accumPrevC must be 1."

    log_progress("generating input_A (INT16, token-contiguous)")

    # =========================================================================
    # input_A  —  INT16, shape (M2, M1, meshRow_A, K1_A, tileSize_A)
    # Stored token-contiguous for DMA scatter; also emit streamer-tiled version.
    # =========================================================================
    A_phys = np.random.randint(
        -256, 255, size=(rM2, rM1, meshRow_A, K1_A, tileSize_A), dtype=np.int16
    )
    A_flat = A_phys.view(np.uint8).reshape(-1)
    pad = (-len(A_flat)) % 64
    if pad:
        A_flat = np.pad(A_flat, (0, pad), constant_values=0)
    data_str += [format_vector_definition("uint8_t", "input_A", A_flat)]

    # Streamer-tiled layout: (M2, M1, K1_A, meshRow_A, tileSize_A)
    A_tiled_phys = np.ascontiguousarray(A_phys.transpose(0, 1, 3, 2, 4))
    A_tiled_flat = A_tiled_phys.view(np.uint8).reshape(-1)
    pad = (-len(A_tiled_flat)) % 64
    if pad:
        A_tiled_flat = np.pad(A_tiled_flat, (0, pad), constant_values=0)
    data_str += [format_vector_definition("uint8_t", "input_A_tiled", A_tiled_flat)]

    # Logical 2D matrix [M_total, K_total] for golden model (use int32 to avoid overflow)
    A_matrix = A_phys.reshape(M_total, K_total).astype(np.int32)

    log_progress("generating router_B (INT4 packed)")

    # =========================================================================
    # router_B  —  INT4 packed, shape (N2, K1, N1, tileSize, meshCol)
    # =========================================================================
    router_B_phys = np.random.randint(
        -7, 7, size=(rN2, rK2 * rK1, rN1, tileSize, meshCol), dtype=np.int8
    )
    data_str += [
        format_vector_definition("uint8_t", "router_B", pack_int4(router_B_phys))
    ]

    log_progress("generating shared expert gate/up B (INT4 packed)")

    # =========================================================================
    # shared_experts_gate_B / shared_experts_up_projection_B
    # Shape per expert: (N2, K1, N1, tileSize, meshCol)
    # Full shape: (num_shared, N2, K1, N1, tileSize, meshCol)
    # =========================================================================
    shared_experts_gate_B_phys = np.random.randint(
        -7,
        7,
        size=(num_shared_experts, sN2, sK2 * sK1, sN1, tileSize, meshCol),
        dtype=np.int8,
    )
    data_str += [
        format_vector_definition(
            "uint8_t",
            "shared_experts_gate_B",
            pack_int4(shared_experts_gate_B_phys),
        )
    ]

    shared_experts_up_projection_B_phys = np.random.randint(
        -7,
        7,
        size=(num_shared_experts, sN2, sK2 * sK1, sN1, tileSize, meshCol),
        dtype=np.int8,
    )
    data_str += [
        format_vector_definition(
            "uint8_t",
            "shared_experts_up_projection_B",
            pack_int4(shared_experts_up_projection_B_phys),
        )
    ]

    log_progress("generating shared expert down B (INT4 packed, 2-VC split)")

    # =========================================================================
    # shared_experts_down_projection_B
    # Down projection uses dual-VC concatenated output (down_meshCol = 2×meshCol).
    # Layout: (num_shared, 2_vc, N2, K1, N1, tileSize, vc_meshCol)
    # The two vc halves are contiguous so dual_vc_gemm can address B0/B1.
    # =========================================================================
    shared_experts_down_projection_B_phys = np.random.randint(
        -7,
        7,
        size=(
            num_shared_experts,
            2,
            sdN2,
            sdK2 * sdK1,
            sdN1,
            tileSize,
            vc_meshCol_down,
        ),
        dtype=np.int8,
    )
    data_str += [
        format_vector_definition(
            "uint8_t",
            "shared_experts_down_projection_B",
            pack_int4(shared_experts_down_projection_B_phys),
        )
    ]

    log_progress("generating individual expert gate/up B (INT4 packed)")

    # =========================================================================
    # individual_experts_gate_B / individual_experts_up_projection_B
    # Shape: (num_indiv, N2, K1, N1, tileSize, meshCol)
    # =========================================================================
    individual_experts_gate_B_phys = np.random.randint(
        -7,
        7,
        size=(num_indiv_experts, iN2, iK2 * iK1, iN1, tileSize, meshCol),
        dtype=np.int8,
    )
    data_str += [
        format_vector_definition(
            "uint8_t",
            "individual_experts_gate_B",
            pack_int4(individual_experts_gate_B_phys),
        )
    ]

    individual_experts_up_projection_B_phys = np.random.randint(
        -7,
        7,
        size=(num_indiv_experts, iN2, iK2 * iK1, iN1, tileSize, meshCol),
        dtype=np.int8,
    )
    data_str += [
        format_vector_definition(
            "uint8_t",
            "individual_experts_up_projection_B",
            pack_int4(individual_experts_up_projection_B_phys),
        )
    ]

    log_progress("generating individual expert down B (INT4 packed, 2-VC split)")

    # =========================================================================
    # individual_experts_down_projection_B
    # Layout: (num_indiv, 2_vc, N2, K1, N1, tileSize, vc_meshCol)
    # =========================================================================
    individual_experts_down_projection_B_phys = np.random.randint(
        -7,
        7,
        size=(num_indiv_experts, 2, idN2, idK2 * idK1, idN1, tileSize, vc_meshCol_down),
        dtype=np.int8,
    )
    data_str += [
        format_vector_definition(
            "uint8_t",
            "individual_experts_down_projection_B",
            pack_int4(individual_experts_down_projection_B_phys),
        )
    ]

    # =========================================================================
    # C (accumulator) — INT32, zero-init or random
    # =========================================================================
    if kwargs["addNonZeroC"] == 1:
        log_progress("generating non-zero C accumulators (INT32)")
        C_MIN, C_MAX = -2147483648, 2147483647

        def make_C(M2, N2, M1, N1):
            return np.random.randint(
                C_MIN, C_MAX, size=(M2, N2, M1, N1, meshRow, meshCol), dtype=np.int32
            )

        router_C = make_C(rM2, rN2, rM1, rN1).reshape(-1)
        data_str += [format_vector_definition("int32_t", "router_C", router_C)]

        shared_up_C = make_C(sM2, sN2, sM1, sN1).reshape(-1)
        data_str += [
            format_vector_definition(
                "int32_t", "shared_experts_up_projection_C", shared_up_C
            )
        ]

        shared_gate_C = make_C(sM2, sN2, sM1, sN1).reshape(-1)
        data_str += [
            format_vector_definition("int32_t", "shared_experts_gate_C", shared_gate_C)
        ]

        shared_down_C = make_C(sdM2, sdN2, sdM1, sdN1).reshape(-1)
        data_str += [
            format_vector_definition(
                "int32_t", "shared_experts_down_projection_C", shared_down_C
            )
        ]

        indiv_up_C = make_C(iM2, iN2, iM1, iN1).reshape(-1)
        data_str += [
            format_vector_definition(
                "int32_t", "individual_experts_up_projection_C", indiv_up_C
            )
        ]

        indiv_gate_C = make_C(iM2, iN2, iM1, iN1).reshape(-1)
        data_str += [
            format_vector_definition(
                "int32_t", "individual_experts_gate_C", indiv_gate_C
            )
        ]

        indiv_down_C = make_C(idM2, idN2, idM1, idN1).reshape(-1)
        data_str += [
            format_vector_definition(
                "int32_t", "individual_experts_down_projection_C", indiv_down_C
            )
        ]

    else:
        log_progress("using NULL C pointers (zero-initialized accumulator)")
        for name in [
            "router_C",
            "shared_experts_up_projection_C",
            "shared_experts_gate_C",
            "shared_experts_down_projection_C",
            "individual_experts_up_projection_C",
            "individual_experts_gate_C",
            "individual_experts_down_projection_C",
        ]:
            data_str += [format_scalar_definition("int32_t *", name, "(int32_t *)NULL")]

    # =========================================================================
    # L15 layout data generation
    # For __snax_bingo_kernel_dual_vc_l15_moe_full, S2 shape, M-direction batching
    # S2: meshRow=2, tileSize=8, meshCol=16
    # Each L15 call = full MoE FFN (Mode-0 SwiGLU + Mode-1 down) for 2 tokens
    # sM2 batches of 2 tokens each = 2*sM2 total tokens
    # =========================================================================
    log_progress("generating L15 layout data for single-cluster S2 overlap test")

    S0_MESHCOL = 4
    S0_TILESIZE = 8

    # L15 globals derived from current shape params
    l15_k0_total = sK1 * tileSize  # 128*8 = 1024
    l15_n0_total = sN1 * meshCol  # sN1*16 = N_hidden
    l15_k1_total = l15_n0_total  # = N_hidden (down K = gate/up N output)
    l15_n1_total = sdN1 * meshCol  # sdN1*16 = down output cols per VC
    l15_m_total = meshRow  # 2 tokens per batch
    l15_k0_bytes = l15_k0_total * 2  # 2048
    l15_k0_s0_tiles = l15_k0_total // S0_TILESIZE  # = 1024/8 = 128  (K方向S0子tile数)
    l15_k1_s0_tiles = (
        l15_k1_total // S0_TILESIZE
    )  # = 512/8  = 64   (down K方向, K_down=N_hidden=512)
    l15_n0_s0_tiles = (
        l15_n0_total // S0_MESHCOL
    )  # = 512/4  = 128  (N方向S0子tile数, N_hidden=sN1*meshCol=32*16=512)
    l15_n1_s0_tiles = (
        l15_n1_total // S0_MESHCOL
    )  # = 256/4  = 64   (down N方向, N_down_vc=sdN1*meshCol=16*16=256)

    L15_LAYOUT_COLORS = {
        "b0_color": 0,
        "b1_color": 272,
        "w2l_color": 128,
        "w2r_color": 0,
        "a_color": 0,
        "d0_color": 0,
        "m1d0_color": 256,
    }
    L15_A_PAD = 32

    def _l15_align_up(v, a=1024):
        return ((int(v) + int(a) - 1) // int(a)) * int(a)

    def _l15_col(off, color=0):
        return _l15_align_up(off) + int(color)

    # Tensor sizes
    _l15_w_bytes = (
        l15_k0_s0_tiles * l15_n0_s0_tiles * 16
    )  # = 128*128*16 = 262144 = 256KB (B0 gate / B1 up 各自, INT4 packed: K=1024×N=512÷2)
    _l15_w2_bytes = (
        l15_k1_s0_tiles * l15_n1_s0_tiles * 16
    )  # = 64*64*16   = 65536  = 64KB  (W2l/W2r 各自, INT4 packed: K_down=512×N_down=256÷2)
    _l15_a_row_stride = l15_k0_bytes + L15_A_PAD  # 2080
    _l15_a_bytes = l15_m_total * _l15_a_row_stride  # 4160
    _l15_mode0_d_bytes = l15_m_total * l15_n0_total * 2  # 1024
    _l15_mode1_pad_bytes = l15_m_total * _l15_a_row_stride  # 4160

    # TCDM placement (matches place_tensors from multi_cluster datagen)
    _l15_delta_b0 = _l15_col(0, L15_LAYOUT_COLORS["b0_color"])
    _l15_delta_b1 = _l15_col(
        _l15_delta_b0 + _l15_w_bytes, L15_LAYOUT_COLORS["b1_color"]
    )
    _l15_delta_w2l = _l15_col(
        _l15_delta_b1 + _l15_w_bytes, L15_LAYOUT_COLORS["w2l_color"]
    )
    _l15_delta_w2r = _l15_col(
        _l15_delta_w2l + _l15_w2_bytes, L15_LAYOUT_COLORS["w2r_color"]
    )
    _l15_delta_a = _l15_col(
        _l15_delta_w2r + _l15_w2_bytes, L15_LAYOUT_COLORS["a_color"]
    )
    _l15_delta_d0 = _l15_col(_l15_delta_a + _l15_a_bytes, L15_LAYOUT_COLORS["d0_color"])
    _l15_delta_m1d0 = _l15_col(
        _l15_delta_d0 + _l15_mode0_d_bytes, L15_LAYOUT_COLORS["m1d0_color"]
    )
    _l15_delta_m1d1 = _l15_delta_m1d0 + l15_n1_total * 2
    _l15_tcdm_end = _l15_delta_m1d0 + _l15_mode1_pad_bytes
    # cfg 结构体（moe_l15_shape_cfg_t，91 个 int32 = 364 bytes）紧接张量数据之后
    _l15_delta_cfg = _l15_tcdm_end
    _l15_cfg_bytes = 91 * 4  # 364 bytes

    l15_placement = {
        "delta_local_a": _l15_delta_a,
        "delta_local_b0": _l15_delta_b0,
        "delta_local_b1": _l15_delta_b1,
        "delta_local_d0": _l15_delta_d0,
        "delta_local_w2l": _l15_delta_w2l,
        "delta_local_w2r": _l15_delta_w2r,
        "delta_local_mode1_d0": _l15_delta_m1d0,
        "delta_local_mode1_d1": _l15_delta_m1d1,
        "tcdm_end": _l15_tcdm_end,
    }

    log_progress(
        f"L15 S2 placement: b0={_l15_delta_b0}, b1={_l15_delta_b1}, "
        f"w2l={_l15_delta_w2l}, w2r={_l15_delta_w2r}, a={_l15_delta_a}, "
        f"d0={_l15_delta_d0}, m1d0={_l15_delta_m1d0}, tcdm_end={_l15_tcdm_end}"
    )

    # ---- L15 S2 shape config (91 int32_t values matching moe_l15_shape_cfg_t) ----
    # Ported from multi_cluster_MoE_datagen.py build_shape_cfg_flat_vals()
    _l15_k_tiles = l15_k0_total // tileSize  # 128
    _l15_k1_tiles = l15_k1_total // tileSize  # 32
    _l15_n0_tiles = l15_n0_total // meshCol  # 16
    _l15_n1_tiles = l15_n1_total // meshCol  # 1

    _l15_mode0_b_n_stride = (meshCol // 4) * l15_k0_s0_tiles * 16  # 4*128*16 = 8192
    _l15_mode1_b_n_stride = (meshCol // 4) * l15_k1_s0_tiles * 16  # 4*32*16  = 2048
    _l15_mode0_b_sstride = l15_k0_s0_tiles * 16  # 2048
    _l15_mode1_b_sstride = l15_k1_s0_tiles * 16  # 512

    _l15_a_channel_en = {0: 0xFFFF, 1: 0x00FF, 2: 0x000F}[array_shape]
    _l15_b_channel_en = {0: 0x03, 1: 0x0F, 2: 0xFF}[array_shape]

    _l15_d_stride1 = 64
    _l15_mode0_d_m_stride = _l15_n0_tiles * _l15_d_stride1  # 16*64 = 1024
    _l15_d_bound0 = 8
    _l15_beats_per_row = meshCol // 4  # 4
    _l15_mode1_a_sstride = {0: [64, 8], 1: [8, 16], 2: [8, 32]}[array_shape]
    _l15_mode1_a_k_stride = {0: 128, 1: 64, 2: 16}[array_shape]

    l15_dev_s2_cfg_vals = [
        # uint32: array_shape, meshRow, tileSize, meshCol, tokens_used
        array_shape,
        meshRow,
        tileSize,
        meshCol,
        meshRow,
        # uint32: M_tiles, K_tiles, N_tiles, K1, N1
        1,
        _l15_k_tiles,
        _l15_n0_tiles,
        _l15_k1_tiles,
        _l15_n1_tiles,
        # int32: mode0_A_sstride[2]
        8,
        _l15_a_row_stride,
        # int32: mode1_A_sstride[2]
        _l15_mode1_a_sstride[0],
        _l15_mode1_a_sstride[1],
        # int32: mode0_B_sstride[2]
        8,
        _l15_mode0_b_sstride,
        # int32: mode1_B_sstride[2]
        8,
        _l15_mode1_b_sstride,
        # int32: D_sstride[1]
        8,
        # int32: mode0_A_tbound[6]
        _l15_k_tiles,
        _l15_n0_tiles,
        1,
        1,
        1,
        1,
        # int32: mode0_A_tstride[6]
        tileSize * 2,
        0,
        meshRow * _l15_a_row_stride,
        0,
        0,
        0,
        # int32: mode1_A_tbound[6]
        _l15_k1_tiles,
        _l15_n1_tiles,
        1,
        1,
        1,
        1,
        # int32: mode1_A_tstride[6]
        _l15_mode1_a_k_stride,
        0,
        _l15_mode0_d_m_stride,
        0,
        0,
        0,
        # int32: mode0_B_tbound[4]
        _l15_k_tiles,
        _l15_n0_tiles,
        1,
        1,
        # int32: mode0_B_tstride[4]
        16,
        _l15_mode0_b_n_stride,
        0,
        0,
        # int32: mode1_B_tbound[4]
        _l15_k1_tiles,
        _l15_n1_tiles,
        1,
        1,
        # int32: mode1_B_tstride[4]
        16,
        _l15_mode1_b_n_stride,
        0,
        0,
        # int32: mode0_D_tbound[4]
        _l15_d_bound0,
        _l15_n0_tiles,
        1,
        1,
        # int32: mode0_D_tstride[4]
        8,
        _l15_d_stride1,
        _l15_mode0_d_m_stride,
        0,
        # int32: mode1_D_tbound[4]
        _l15_beats_per_row,
        meshRow,
        _l15_n1_tiles,
        1,
        # int32: mode1_D_tstride[4]
        8,
        _l15_a_row_stride,
        meshCol * 2,
        0,
        # int32: A_channel_en[1], B_channel_en[1], D_channel_en[1]
        _l15_a_channel_en,
        _l15_b_channel_en,
        1,
        # int32: delta_local_a, b0, b1, d0, w2l, w2r, mode1_d0, mode1_d1
        _l15_delta_a,
        _l15_delta_b0,
        _l15_delta_b1,
        _l15_delta_d0,
        _l15_delta_w2l,
        _l15_delta_w2r,
        _l15_delta_m1d0,
        _l15_delta_m1d1,
        # int32: tcdm_end, mode0_output_elems, mode1_output_elems
        _l15_tcdm_end,
        meshRow * l15_n0_total,
        meshRow * l15_n1_total,
        # int32: mode1_output_row_stride_bytes, mode1_padded_output_elems
        _l15_a_row_stride,
        meshRow * (_l15_a_row_stride // 2),
    ]
    assert (
        len(l15_dev_s2_cfg_vals) == 91
    ), f"Expected 91 cfg vals, got {len(l15_dev_s2_cfg_vals)}"
    cfg_vals_str = ", ".join(str(v) for v in l15_dev_s2_cfg_vals)
    data_str += [
        f"// L15 device shape config for S2 — matches moe_l15_shape_cfg_t (91 int32_t values)\n"
        f"static const int32_t l15_dev_s2_cfg[91] = {{\n    {cfg_vals_str}\n}};"
    ]

    # ---- B0 (gate) and B1 (up) weights: S0-tile format, constant values ----
    # INT4 packed: B0 = all 1s (0x11 per byte), B1 = all 2s (0x22 per byte)
    _l15_num_b_tiles = l15_k0_s0_tiles * l15_n0_s0_tiles  # 128*64 = 8192 tiles
    _l15_b0_packed = np.full(
        _l15_num_b_tiles * 16, 0x11, dtype=np.uint8
    )  # 131072 bytes
    _l15_b1_packed = np.full(
        _l15_num_b_tiles * 16, 0x22, dtype=np.uint8
    )  # 131072 bytes
    data_str += [format_vector_definition("uint8_t", "l15_weights_b0", _l15_b0_packed)]
    data_str += [format_vector_definition("uint8_t", "l15_weights_b1", _l15_b1_packed)]

    # ---- W2l / W2r：Mode-1 down projection 权重（各 2048 bytes，单份，所有 batch 共用）----
    # iDMA: W2l → TCDM[base + delta_w2l=263296], size=2048
    # xDMA: W2r → TCDM[base + delta_w2r=266240], size=2048（与 W2l 并行，dual_dma）
    _l15_num_w2_tiles = (
        l15_k1_s0_tiles * l15_n1_s0_tiles
    )  # 32×4 = 128 tiles × 16B = 2048B
    _l15_w2l_data = np.full(_l15_num_w2_tiles * 16, 0x11, dtype=np.uint8)  # INT4 packed
    _l15_w2r_data = np.full(_l15_num_w2_tiles * 16, 0x22, dtype=np.uint8)  # INT4 packed
    data_str += [format_vector_definition("uint8_t", "l15_weights_w2l", _l15_w2l_data)]
    data_str += [format_vector_definition("uint8_t", "l15_weights_w2r", _l15_w2r_data)]

    # ---- A_padded：输入激活（单份，所有 batch 共用，pre-load 进 ping/pong） ----
    # idma: A → TCDM[base + delta_a=268288], size=4160（pipeline 开始前各 buffer 预加载一次）
    # A 区域不被 dma_B（写 [0..262416]）或 dma_W2（写 [263296..268288]）覆盖，
    # 因此 pre-load 后整个 pipeline 期间保持有效。
    K_total = l15_k0_total  # 1024
    _a_row_elems = _l15_a_row_stride // 2  # 1040 int16 per row
    _a_logical = A_phys[0, 0].reshape(meshRow, K_total)  # (2, 1024) int16，用第一份数据
    _a_padded_arr = np.zeros((meshRow, _a_row_elems), dtype=np.int16)
    _a_padded_arr[:, :K_total] = _a_logical
    _l15_a_data = _a_padded_arr.view(np.uint8).flatten()  # 4160 bytes
    data_str += [format_vector_definition("uint8_t", "l15_input_a", _l15_a_data)]

    log_progress(
        f"L15 separate arrays: B0/B1={_l15_w_bytes}B each, "
        f"W2l/W2r={_l15_num_w2_tiles * 16}B each, "
        f"A_padded={len(_l15_a_data)}B; TCDM offsets: "
        f"b0={_l15_delta_b0}, b1={_l15_delta_b1}, "
        f"w2l={_l15_delta_w2l}, w2r={_l15_delta_w2r}, a={_l15_delta_a}"
    )

    # ---- Scalar constants for main_bingo.py L15 handles/DFG ----
    for _nm, _val in [
        ("l15_w_bytes", _l15_w_bytes),  # 131072 = B0/B1 各自大小
        ("l15_delta_b1", _l15_delta_b1),  # 131344 = B1 TCDM 偏移
        ("l15_delta_w2l", _l15_delta_w2l),  # 263296 = W2l TCDM 偏移
        (
            "l15_delta_w2r",
            _l15_delta_w2r,
        ),  # 266240 = W2r TCDM 偏移（第二次 dual_dma xDMA 目标）
        ("l15_delta_a", _l15_delta_a),  # 268288 = A   TCDM 偏移（pre-load idma 目标）
        ("l15_w2_bytes", _l15_w2_bytes),  # 2048   = W2l/W2r 各自大小
        ("l15_a_bytes", _l15_a_bytes),  # 4160   = A_padded 大小
        (
            "l15_delta_cfg",
            _l15_delta_cfg,
        ),  # cfg 结构体在 TCDM 中的偏移（紧接张量数据之后）
        ("l15_cfg_bytes", _l15_cfg_bytes),  # moe_l15_shape_cfg_t 大小 = 364 bytes
        (
            "l15_tcdm_size",
            _l15_tcdm_end + _l15_cfg_bytes,
        ),  # 总 TCDM 大小（含 cfg 区域）
    ]:
        data_str += [format_scalar_definition("uint32_t", _nm, _val)]

    if not full_golden_data:
        log_progress("skipping golden tensors — emitting placeholder layer_output")
        data_str += [
            format_vector_definition(
                "int8_t", "layer_output", np.zeros(1, dtype=np.int8)
            )
        ]
        return data_str

    # =========================================================================
    # Golden model (for --full_golden_data verification)
    # Dequantized INT16×INT4 → INT32 accumulation
    # =========================================================================
    log_progress("computing full golden tensors")

    def dequant_b(b_phys_int8):
        """Treat INT4-range INT8 values as INT4 weights (no further scaling)."""
        return b_phys_int8.astype(np.int32)

    # ---- Router: A @ router_B ----
    # A_matrix: (M_total, K_total) int32
    # router_B logical: (N_router, K_total) int32
    rB_mat = (
        router_B_phys.transpose(0, 2, 4, 1, 3)  # (N2, N1, meshCol, K1, tileSize)
        .reshape(N_router, K_total)
        .astype(np.int32)
    )
    router_scores = A_matrix @ rB_mat.T  # (M_total, N_router)

    top_k_indices, top_k_probability = softmax_golden_model(
        router_scores,
        top_k,
        float(MoE_config["softmax_scale"]),
    )
    log_progress(
        f"top_k_indices shape: {top_k_indices.shape}, unique experts: {np.unique(top_k_indices)}"
    )

    # ---- Shared experts (all tokens) ----
    scale_in = float(MoE_config["swish_glu_scale_in"])
    scale_out = float(MoE_config["swish_glu_scale_out"])

    def compute_gemm(A_mat_2D, B_phys, M1, K1, N1, meshRow, tileSize, meshCol):
        """Simple reference GEMM: A (M, K) × B (K, N) → D (M, N) in int32."""
        # B_phys shape: (N2, K1_total, N1, tileSize, meshCol)
        N2_val = B_phys.shape[0]
        K_total_loc = B_phys.shape[1] * tileSize
        N_total_loc = N2_val * N1 * meshCol
        B_mat = (
            B_phys.transpose(0, 2, 4, 1, 3)  # (N2, N1, meshCol, K1, tileSize)
            .reshape(N_total_loc, K_total_loc)
            .astype(np.int32)
        )
        return A_mat_2D @ B_mat.T  # (M_total, N_total_loc)

    all_experts_out = []

    # Shared experts
    for se in range(num_shared_experts):
        gate_B = shared_experts_gate_B_phys[se]  # (sN2, sK1, sN1, tileSize, meshCol)
        up_B = shared_experts_up_projection_B_phys[se]
        gate_D = compute_gemm(
            A_matrix, gate_B, sM1, sK1, sN1, meshRow, tileSize, meshCol
        )
        up_D = compute_gemm(A_matrix, up_B, sM1, sK1, sN1, meshRow, tileSize, meshCol)
        activated = swish_glu_golden_model(gate_D, up_D, scale_in, scale_out)

        # down projection: (M_total, N_hidden) × (N_hidden, N_out) → (M_total, N_out)
        # down_B_phys: (2_vc, N2, K1, N1, tileSize, vc_meshCol)
        down_B_phys = shared_experts_down_projection_B_phys[
            se
        ]  # (2, sdN2, sdK1, sdN1, ts, vc_mc)
        # Concatenate two VC halves along meshCol axis → (sdN2, sdK1, sdN1, ts, meshCol_down)
        vc0 = down_B_phys[0]  # (sdN2, sdK1, sdN1, ts, vc_mc)
        vc1 = down_B_phys[1]
        # Interleave along meshCol dim → (sdN2, sdK1, sdN1, ts, vc_mc*2)
        down_B_full = np.concatenate(
            [vc0, vc1], axis=-1
        )  # (sdN2, sdK1, sdN1, ts, meshCol_down)
        down_D = compute_gemm(
            activated.astype(np.int32),
            down_B_full,
            sdM1,
            sdK1,
            sdN1,
            meshRow,
            tileSize,
            meshCol_down,
        )
        all_experts_out.append(down_D)

    # Individual experts (only tokens assigned by router)
    indiv_out = np.zeros((M_total, idN2 * idN1 * meshCol_down), dtype=np.int64)
    for m in range(M_total):
        for k_idx in range(top_k):
            expert_id = int(top_k_indices[m, k_idx])
            if expert_id >= num_indiv_experts:
                continue  # out of valid expert range (padding)
            prob = int(top_k_probability[m, k_idx])

            gate_B = individual_experts_gate_B_phys[expert_id]
            up_B = individual_experts_up_projection_B_phys[expert_id]
            token_A = A_matrix[m : m + 1]  # (1, K_total)
            gate_D = compute_gemm(
                token_A, gate_B, iM1, iK1, iN1, meshRow, tileSize, meshCol
            )
            up_D = compute_gemm(
                token_A, up_B, iM1, iK1, iN1, meshRow, tileSize, meshCol
            )
            activated_tok = swish_glu_golden_model(
                gate_D, up_D, scale_in, scale_out
            )  # (1, N_hidden)

            down_B_phys = individual_experts_down_projection_B_phys[
                expert_id
            ]  # (2, N2, K1, N1, ts, vc_mc)
            vc0 = down_B_phys[0]
            vc1 = down_B_phys[1]
            down_B_full = np.concatenate([vc0, vc1], axis=-1)
            down_D_tok = compute_gemm(
                activated_tok.astype(np.int32),
                down_B_full,
                idM1,
                idK1,
                idN1,
                meshRow,
                tileSize,
                meshCol_down,
            )  # (1, N_out)
            # Weighted accumulation (probability in fixed-point scaled by softmax_scale)
            indiv_out[m] += down_D_tok[0].astype(np.int64) * prob

    # Scale down by softmax_scale and combine shared + individual outputs
    softmax_scale_val = float(MoE_config["softmax_scale"])
    indiv_out_scaled = (indiv_out.astype(np.float64) / softmax_scale_val).astype(
        np.int32
    )

    # Sum shared expert outputs (all tokens receive shared expert output)
    shared_sum = np.zeros((M_total, N_out), dtype=np.int32)
    for d in all_experts_out:
        shared_sum += d

    # Final output: shared + individual
    final_out_f = (shared_sum + indiv_out_scaled).astype(np.float64)
    final_out_f /= 2 ** int(MoE_config["final_shift_step"])
    layer_output_2D = np.clip(final_out_f, -128, 127).astype(np.int8)

    data_str += [
        format_vector_definition("int8_t", "layer_output", layer_output_2D.reshape(-1))
    ]

    return data_str


def main():
    parser = argparse.ArgumentParser(
        description="Generating data for single_cluster_MoE (INT16×INT4)"
    )
    parser.add_argument(
        "-c",
        "--cfg",
        type=pathlib.Path,
        required=True,
        help="Workload param config file (params.hjson)",
    )
    parser.add_argument(
        "--hwcfg",
        type=pathlib.Path,
        required=True,
        help="Hardware config file (hw_cfg.hjson)",
    )
    parser.add_argument(
        "--emit_mini_golden", action="store_true", help="(compatibility flag, ignored)"
    )
    parser.add_argument(
        "--num_double_buffers",
        type=int,
        default=None,
        help="(compatibility flag, ignored)",
    )
    parser.add_argument(
        "--full_golden_data",
        action="store_true",
        help="Emit full golden tensors for verification.",
    )
    args = parser.parse_args()

    with args.cfg.open() as f:
        param = hjson.loads(f.read())
    with args.hwcfg.open() as f:
        hw = hjson.loads(f.read())

    merged = {**param, **hw}
    merged["full_golden_data"] = args.full_golden_data

    print(emit_header_file(**merged))


if __name__ == "__main__":
    main()
