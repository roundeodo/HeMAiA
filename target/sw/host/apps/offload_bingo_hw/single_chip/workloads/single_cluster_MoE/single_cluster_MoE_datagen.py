# single_cluster_MoE datagen — rewritten for dual-VC INT16×INT4 hardware
#
# Hardware: snax_dual_versacore_int16x4_rebalanced_cluster
#   A: INT16 (2 bytes/element)
#   B: INT4 nibble-packed (2 values per byte, lo=even idx, hi=odd idx)
#   D: INT32 (4 bytes/element)
